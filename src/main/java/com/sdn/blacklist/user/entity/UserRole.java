package com.sdn.blacklist.user.entity;

public enum UserRole {

    // ══════════════════════════════════════════
    // مستوى المنصّة
    // ══════════════════════════════════════════
    SUPER_ADMIN,        // أنت — كل الشركات وكل البيانات، بلا tenant

    // ══════════════════════════════════════════
    // مستوى الشركة / البنك (tenant)
    // ══════════════════════════════════════════
    COMPANY_ADMIN,      // مدير الشركة / البنك — رأس الشركة، بيشوف كلشي، بيحدد السياسات، وبيعيّن

    // ── خط الالتزام ──
    COMPLIANCE_MANAGER, // مدير الالتزام — بيعيّنو مدير الشركة، صاحب القرار النهائي على الحالة
    COMPLIANCE_OFFICER, // ضابط الالتزام — بيعيّنو مدير الالتزام، بكل فرع، بيدرس ويحط ملاحظة

    // ── خط الأفرع ──
    BRANCH_MANAGER,     // مدير الفرع — بيعيّنو مدير الشركة، بيمرّر الحالة للأعلى
    TELLER,             // الكونتوار — بيعيّنو مدير الفرع، بيرفع الحالة من عند الزبون

    // ══════════════════════════════════════════
    // legacy — للتوافق مع البيانات القديمة، بينشال لاحقاً إذا طلع مو مستعمل
    // ══════════════════════════════════════════
    SUBSCRIBER
}